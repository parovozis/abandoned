<h2>Очередь</h2>
<?php title ('Q0011', 'Что такое очередь?' ) ?>

Очередь - это фотографии, отосланные  в «Галерею», но ещё не опубликованные. 

<?php title ('Q0020', 'Все ли фотографии попадают в очередь?' ) ?>

Нет. В очередь попадают только конкурсные фотографии - те, которые участвуют в конкурсе «Фото дня». Фотографии, не участвующие в конкурсе, публикуются модераторами по мере расcмотрения. 

<?php title ('Q0021', 'Кто решает, участвует ли фотография в конкурсе &laquo;Фото дня&raquo;?' ) ?>

Вы. При отсылке фотографии пометьте поле «Вне конкурса» - и присланная Вами фотография пойдёт вне конкурса. 

<?php title ('Q0004', 'Сколько фотографий я могу отослать за один день?')
?>

Это зависит от длины очереди. Чем длиннее очередь, тем меньше конкурсных фотографий может быть опубликовано ежедневно. Это сделано для того, чтобы автоматически регулировать наплыв фотографий: ведь чем больше фотографий в очереди, тем дольше они ждут! 
<p>
Авторы, публикующие свои фотографии впервые, получают три очка и могут прислать от одной до трёх конкурсных фотографий. После этого за каждый день, когда в очереди меньше 156 неопубликованных фотографий, все авторы в получают по одному дополнительному очку (право опубликовать ещё одно фото), но всего не более трёх очков. Кроме того, по одному очку засчитывается всем авторам в ночь с субботы на воскресенье. 
<p>
Внеконкурсные фотографии могут быть присланы в неограниченных количествах. 

<?php title ('Q0016', 'Почему неопубликованные фотографии пропадают из очереди?') ?>

Их удаляет модератор за несоответствие требованиям «Галереи». За каждую удалённую конкурсную фотографию автору возвращается одно очко. 
<p>
Для окончательного удаления фото из галереи требуется консенсус как минимум двух модераторов. Фотография, представленная к удалению, помещается в соответствующий раздел в «Мастерской». Впоследствии она может быть как удалена окончательно (модератором или автором), так и опубликована. 


<?php title ('Q0007', 'Могу ли я подредактировать описание отосланной фотографии?') ?>
Да. Щёлкните по иконке «Редактировать эту фотографию». Исправьте описание фотографии и нажмите на кнопку «Изменить». 

<?php title ('Q0012', 'Могу ли я удалить фотографию?') ?>
Фотография может быть удалена только путем обращения к администратору/модератору.

<h2>Модерация и публикация фотографий</h2>

<?php title ('Q1001', 'Каким образом назначаются модераторы?') ?>
Модераторов фотогалереи назначает Администратор, как по собственной инициативе, так и путем рассмотрения предложений от других модераторов или участников. В любом случае окончательное решение о назначении/удалении принимает Администратор, он и только он.

<?php title ('Q1002', 'Каковы права и обязанности модератора?') ?>

Модератор обязан: 
<ul>
<li>Объективно и непредвзято оценивать качество и информационную ценность фотографий, четко представляя критерии таковых;
<li>Публиковать, представлять к удалению, удалять фотографии исключительно исходя из критериев, прописанных в пунктах раздела ”Отсылка фотографий”;
<li>Исправлять/дополнять описания, не соответствующие техническим требованиям к подписям;
<li>Отслеживать комментарии, вычленяя из них и удаляя неадекватные и запрещенные;
</ul>
Исходя из общепринятых принципов модераторской этики быть предельно корректным в высказываниях и поступках, не допускать какой-либо предвзятости в своих действиях и минимизировать субъективность как при оценке фотографий, так и расcмотрении комментариев и вынесении штрафных санкций.
<p>
Модератор имеет право:

<ul>
<li>Опубликовать, представить к удалению или удалить окончательно за несоответствие правилам галереи ту или иную фотографию из очереди;
<li>Представить к удалению или удалить окончательно за несоответствие правилам галереи ту или иную фотографию из числа опубликованных посредством автомодерации;
<li>Изменить описание любой фотографии в галерее, добавить координаты места съемки;
<li>Удалить неадекватный или запрещенный комментарий;
<li>Вынести устное или официальное предупреждение, а также заблокировать аккаунт пользователя исходя из пунктов раздела ”Штрафные санкции”
</ul>

