<?php title ('Q0019', 'Каковы технические требования к подписям?' ) ?>

Каждая фотография должна иметь точную и лаконичную подпись, соответствующую содержанию фотографии. Для фотографий подвижного состава обязательно указание типа подвижного состава (электровоз, тепловоз, электропоезд и т.п.), серии и номера локомотива, единицы МВПС или путевой машины (за исключением снимков с нечитаемым номером) и места съемки (название станции, перегона, принадлежность подъездного пути). Для ВСЕХ фотографий обязательно указание объекта, места, региона и даты съемки в пределах точности, известной автору. Дополнительная информация приветствуется в краткой и лаконичной форме. В ограниченных количествах допускается краткое художественное название фотографии (в кавычках и перед основной частью описания), подчеркивающее его информационный или художественный смысл. Перед загрузкой фото необходимо выбрать из выпадающего списка категорию, которой оно принадлежит (не менее одной категории). Модератор имеет право дополнить описание, сократить его, внести необходимые коррективы,  удалить художественное название, а также окончательно удалить незадокументированное или недостаточно задокументированное фото. 
<p>
Если на фото изображён  электровоз ВЛ10У-1110, то подпись должна быть “Электровоз ВЛ10У-1110”.  Серии локомотивов и МВПС (включая индексы) записывайте только заглавными буквами и слитно. Номера локомотивов и МВПС отделяйте от серии дефисом без пробелов. Правильно: ЧС2Т-1045. Неправильно: ЧС2Т - 1045; Чс2т - 1045; Чс2Т-1045; ЧС 2Т-1045. Номера в названиях станций записывайте римскими цифрами, через дефис и без пробелов. Правильно: Поварово-III. Неправильно: Поварово-3; Поварово III; Поварово 3; Поварово - III. 

