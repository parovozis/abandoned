Конкурсные фотографии публикуются ежесуточно, в 3.00 по московскому времени, по 48 фотографий за раз. Одновременно с этим публикуются неконкурсные фотографии, в количестве пропущенном модераторами накануне, т.е. в течении минувших 24 часов от 3.00.00 до 2.59.59.

