<h2>Штрафные санкции</h2>
<?php title ('Q0030', 'Что такое предупреждение?' ) ?>
Предупреждение может быть вынесено модератором за неадекватный или запрещенный  комментарий. Срок действия предупреждения - 30 дней. Авторы, получившие два и более непогашенных предупреждения, лишаются права оставлять комментарии. Авторы, получившие 5 и более предупреждений, лишаются права публиковать фотографии. Предупреждения отмечаются чёрными крестиками рядом с именем автора в "Моей фотогалерее". Детали предупреждений (кто вынес, когда вынес, когда истекает срок действия) можно посмотреть в Моей учётной записи. Предупреждения действительны во всех галереях сайта. 

<?php title ('Q0031', 'Мой аккаунт заблокирован. За что?' ) ?>
Аккаунты блокируются модераторами :
<ul>
<LI>на 30 дней с возможностью разблокировки за запрещенные или в исключительных случаях за систематические неадекватные комментарии;
<li>на 30 дней с возможностью блокировки за систематическую повторную загрузку отклоненных фотографий без видимых изменений;
<li> без возможности разблокировки либо на срок, определяемый Администратором за публикацию чужих фотографий без ведома автора, доказанные накрутки/антинакрутки при голосовании и систематические нарушения правил поведения и публикации в фотогалерее (в том числе систематические запрещенные комментарии).
</ul>
<?php title ('Q1010', 'Могу ли я использовать альтернативные учетные записи при блокировке основной?' ) ?>

При блокировке комментариев автор теряет право оставлять комментировать фотографии посредством иных учетных записей, к которым он имеет доказанное отношение. При блокировке аккаунта автор теряет право публиковать фотографии и оставлять комментарии посредством иных учетных записей, к которым он имеет доказанное отношение. Запрет распространяется как на дублирующие, так и на совмещенные аккаунты и имеет срок действия равный сроку блокировок основного аккаунта пользователя. 

	
