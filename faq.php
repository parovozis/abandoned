<?php title ('Q0010', 'Редактируются ли фотографии перед публикацией?') ?>

Перед публикацией, как правило, администратор/модератор редактирует описание фотографии: проверяет (по мере возможностей) правильность номеров подвижного состава и региона съёмки и сокращает «раздутые» и излишне «художественные» подписи. Кроме того, файлы более 1080000 пикселей по площади автоматически сжимаются до 1080000 пикселей без гарантии качества, а в основной галерее в один из углов (на Ваше усмотрение) впечатывается логотип «Галереи». 

<?php title ('Q0015', 'Сохраняются ли при загрузке в Галерею метаданные в формате EXIF?') ?>
Да. Отсутствие метаданных на странице с фотографией означает их отсутствие в исходном (загружаемом) файле.

<?php title ('Q0009', 'Как узнать, какие фотографии будут точно опубликованы?') ?>

Зайдите в «Мою мастерскую» и выберите раздел «Мои неопубликованные фотографии». В подразделе «Фотографии, которые будут опубликованы завтра» находятся фотографии, которые точно будут опубликованы. 

<?php title ('Q0029', 'Можно ли опубликовать фотографии в определённый день или в определённой последовательности?') ?>

Модераторы не могут гарантировать, что конкретная фотография будет опубликована в конкретный день либо одновременно с какой-либо другой фотографией, а также что присланные фотографии будут опубликованы в определённой последовательности. Фотографии утверждаются к публикации по мере рассмотрения модератором. Изменение даты публикации после утверждения снимка запрещено и возможно только в экстраординарных случаях, рассматриваемых администратором отдельно.

<?php title ('Q1003', 'Что такое автомодерация?') ?>
Автомодерация – это возможность определенным авторам публиковать неконкурсные фотографии автоматически, без рассмотрения их модератором. Неконкурсное фото при этом принимается к публикации сразу после загрузки его в галерею. Режим автомодерации включен Администратором для ускорения обработки очереди, разгрузки объема работы модераторов и является кредитом доверия авторам, чьи снимки минимально удаляются, а описания минимально корректируются модераторами. 

<?php title ('Q1004', 'Какими показателями определяется автомодерация?') ?>

Автомодерация определяется количеством внутренних баллов (баллов автомодерации), начисляемых конкретному автору. Начисление происходит следующим образом:
<ul>
<li>Утверждение снимка модератором    +1 балл
<li>Исправление описания модератором    -1 балл
<li>Исправление описания автором  - без изменения (0 баллов)
<li>Удаление модератором представленного к удалению снимка при положительном балансе -  обнуление баланса
<li>Удаление модератором представленного к удалению снимка при отрицательном балансе     -5 баллов
<li>Удаление модератором представленного к удалению снимка с указанием причины ”По просьбе автора” –  без изменений (0 баллов)
<li>Удаление автором  представленного к удалению снимка -  без изменений (0 баллов)
<li>Авторам с отрицательным балансом ежедневно +1 балл
</ul>
Автомодерация включается при  достижении автором баланса 26 баллов и  выключается при снижении количества баллов до 25 и ниже. Удаление модератором представленного к удалению снимка при любом положительном балансе ведет к его обнулению и отключению автомодерации, если таковая действовала. При отрицательном балансе  -65 баллов и ниже автор получает предупреждения при загрузке фотографий. При отрицательном балансе  -131 балл и ниже у автора временно блокируется возможность загрузки фотографий в галерею.   
<p>
Количество внутренних баллов того или иного автора известно только модераторам/администратору. Т.к. данная информация является внутренней, модератор/администратор вправе не давать её авторам. 


<h2>Голосование за Фото Дня</h2>
<?php title ('Q0001', 'Как проголосовать за Фото Дня?' ) ?>
Зайдите в &laquo;<a href="ac_authorize.php">Свою мастерскую</a>&raquo;, потом перейдите по ссылке &laquo;Новые фото&raquo;. Имейте в виду, что голосовать в этой галерее могут только авторы, которые опубликовали хотя бы одну конкурсную фотографию.

<?php title ('Q0018', 'Почему я не могу проголосовать за некоторые фотографии?' ) ?>
Авторы этих фотографий предпочли не участвовать в конкурсе &laquo;Фото Дня&raquo;.

<?php title ('Q1005', 'Могу ли я использовать альтернативные учетные записи при голосовании?' ) ?>

Технически возможно голосование посредством любого аккаунта, имеющего не менее одной конкурсной фотографии. При этом доказанное систематическое голосование  автора из основного и альтернативных (в том числе и совместных) аккаунтов за одного и того же автора (или группу авторов) характеризуется как накрутка, а против одного и того же автора (группы авторов) – как антинакрутка. Альтернативные учетные записи, доказанно зарегистрированные с целью накруток или антинакруток, признаются администратором фиктивными. Фиктивные аккаунты блокируются без возможности разблокировки или удаляются Администратором. Аккаунты авторов, причастных к фиктивному голосованию, блокируются на срок, определяемый Администратором.
<p>
Накрутка – доказанное Администратором систематическое голосование одного и того же участника посредством нескольких учетных записей, в том числе фиктивных, с целью достижения максимального количества баллов у снимков определенного автора или группы авторов и таким образом обеспечения любой  ценой победу последних в конкурсе ”Фото Дня”.
<p>
Антинакрутка - доказанное Администратором систематическое голосование одного и того же автора посредством нескольких учетных записей, в том числе фиктивных, с целью достижения максимального количества баллов у всех снимков, кроме снимков определенного автора или группы авторов и таким образом обеспечения любой ценой проигрыш последних в конкурсе ”Фото Дня”.
<p>
Понимая всю условность понятий "накрутка", "антинакрутка" и "фиктивный аккаунт",  модераторы  тем не менее берут на себя ответственность за фиксацию случаев фиктивного голосования, авторов, в нём участвующих, и доведение этой информации до Администратора. Окончательно решение о наличии факта накрутки или антинакрутки (т.е. докзанность таковых), а также о применении штрафных санкций к авторам, причастных к фиктивному голосованию, принимает Администратор. Он и только он. 

